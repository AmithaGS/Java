package OOPs;

public class If_Else_If {

	public static void main(String[] args) {
		int marks=25;
		if(marks>=91) {
			System.out.println("Get a car");
		}
		else if(marks>=81) {
			System.out.println("Get a Bike");
		}
		else if(marks>=71) {
			System.out.println("Get a Laptop");
		}
		else if(marks>=61) {
			System.out.println("Get a SmartPhone");
		}
		else if(marks>=41) {
			System.out.println("Get a Bicycle");
		}
		else {
			System.out.println("Get lost");

		}
	}

}

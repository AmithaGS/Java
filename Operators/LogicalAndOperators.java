class LogicalAndOperators{

      //work on boolean values
	public static void main(String[] args){
		
		boolean res1=true && true;
		System.out.println("true && true result is: "+res1);
		
        boolean res2=true && false;
		System.out.println("true && false result is: "+res2);
		
        boolean res3=false && true;	
	    System.out.println("false && true result is: "+res3);

        boolean res4=false && false;	
	    System.out.println("false && false result is: "+res4);
	}
}